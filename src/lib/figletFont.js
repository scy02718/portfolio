export const figlet = (text) => {
    const chars = (text || '').toUpperCase().split('').map((c) => F[c] || F[' '])
    if (chars.length === 0) return []
    const rows = []
    for (let r = 0; r < 5; r++) {
        rows.push(chars.map((c) => c[r]).join(' '))
    }
    return rows
}
